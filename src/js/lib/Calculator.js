module.exports = class Calculator {
    //adds add function
    add(x, y) {
      return x + y;
    }
    //adds subtract function
    subtract(x, y) {
        return x - y;
      }
    //adds multiply function
    multiply(x, y) {
        return x * y;
      }
    //adds divide function
    divide(x, y) {
        return x / y;
      }
  }